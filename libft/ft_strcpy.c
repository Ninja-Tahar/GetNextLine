#include "libft.h"

char		*ft_strcpy(char *dst, const char *src)
{
	char	*p;
	int		i;

	i = 0;
	p = dst;
	while (src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
