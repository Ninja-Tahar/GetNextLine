char	*ft_strrev(char *str)
{
	int		i;
	int		n;
	char	aux;

	i = 0;
	n = 0;
	while (str[i])
		i++;
	i--;
	while (n <= i)
	{
		aux = str[i];
		str[i] = str[n];
		str[n] = aux;
		n++;
		i--;
	}
	return (str);
}
